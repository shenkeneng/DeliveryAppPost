package com.frxs.delivery.model;

import java.io.Serializable;

/**
 * 商品信息 by Tiepier
 */
public class ProdcutDetailList implements Serializable {


    private static final long serialVersionUID = -928837936939300807L;

    private int ProductId;//商品ID
    private String ProductName;//商品名称
    private String SKU;//商品编码
    private String BarCode;//条码
    private Double SaleQty;//配送数量
    private String Remark;//备注
    private int ShelfAreaID;//货区ID
    private String ShelfAreaCode;//货区编码
    private String ShelfAreaName;//货区名称
    private String SaleUnit;//数量单位

    public int getProductId() {
        return ProductId;
    }

    public void setProductId(int productId) {
        ProductId = productId;
    }

    public String getProductName() {
        return ProductName;
    }

    public void setProductName(String productName) {
        ProductName = productName;
    }

    public String getSKU() {
        return SKU;
    }

    public void setSKU(String SKU) {
        this.SKU = SKU;
    }

    public String getBarCode() {
        return BarCode;
    }

    public void setBarCode(String barCode) {
        BarCode = barCode;
    }

    public Double getSaleQty() {
        return SaleQty;
    }

    public void setSaleQty(Double saleQty) {
        SaleQty = saleQty;
    }

    public String getRemark() {
        return Remark;
    }

    public void setRemark(String remark) {
        Remark = remark;
    }

    public int getShelfAreaID() {
        return ShelfAreaID;
    }

    public void setShelfAreaID(int shelfAreaID) {
        ShelfAreaID = shelfAreaID;
    }

    public String getShelfAreaCode() {
        return ShelfAreaCode;
    }

    public void setShelfAreaCode(String shelfAreaCode) {
        ShelfAreaCode = shelfAreaCode;
    }

    public String getShelfAreaName() {
        return ShelfAreaName;
    }

    public void setShelfAreaName(String shelfAreaName) {
        ShelfAreaName = shelfAreaName;
    }

    public String getSaleUnit() {
        return SaleUnit;
    }

    public void setSaleUnit(String saleUnit) {
        SaleUnit = saleUnit;
    }
}
